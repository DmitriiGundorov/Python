# Задача №15. Иван Васильевич пришел на рынок и решил купить два арбуза:
# один для себя, а другой для тещи. Понятно, что для себя нужно выбрать арбуз
# потяжелей, а для тещи полегче. Но вот незадача: арбузов слишком много и он не знает как же выбрать
# самый легкий и самый тяжелый арбуз? Помогите ему! Пользователь вводит одно число N – количество
# арбузов. Вторая строка содержит N чисел, записанных на новой строчке каждое. Здесь каждое
# число – это масса соответствующего арбуза
# Input: 5 -> 5 1 6 5 9
# Output: 1 9

print("----------|  Задача 15  |----------")
n = int(input("Введите количество арбузов: "))
temp = 15
count_max = 0
count_min = temp
for i in range(n):
    weight_watermelon = random.randint(1, temp)
    # print(weight_watermelon)
    if weight_watermelon > count_max:
        count_max = weight_watermelon
    if weight_watermelon < count_min:
        count_min = weight_watermelon

print(
    f"Максимальная масса арбуза, кг -> {count_max}\nМинимальная масса арбуза, кг -> {count_min}")
