# Задача 9. По данному целому неотрицательному n вычислите
# значение n!. N! = 1 * 2 * 3 * … * N (произведение всех
# чисел от 1 до N) 0! = 1 Решить задачу используя цикл while
# Input: 5
# Output: 120

# Решение задачи 9.

import random
print("----------|  Задача 9  |----------")
n = int(input("Введите значение факториала: "))
N = 1
while (n > 0):
    N *= n
    n -= 1
print(f"Значение факториала -> {N}")


# Задача №11. Решение в группах
# Дано натуральное число A > 1. Определите, каким по счету числом Фибоначчи оно является, то есть
# выведите такое число n, что φ(n)=A. Если А не является числом Фибоначчи, выведите число -1.
# Input: 5
# Output: 6

print("----------|  Задача 11  |----------")

def fib_n(n: int) -> int:
    previous_n = 0
    next_n = 1
    fib_n = 0
    for i in range(n-1):
        fib_n = previous_n + next_n
        previous_n, next_n = next_n, fib_n
    return fib_n

n = int(input("Введите натуральное число: "))

flag = True
i = 1
while (flag):
    temp = fib_n(i)
    if temp < n:
        i += 1
    elif temp == n:
        print(f"Номер числа Фиббоначи -> {i}")
        flag = False
    else:
        print(f"Такого числа Фиббоначи нет")
        print(-1)
        flag = False


